                           <h1>Sejarah SMK Wikrama</h1>
                           <p class="paragraf">SMK Wikrama Bogor didirikan oleh Ir. Itasia Dina Sulvianti dan Dr.H.RP Agus Lelana dibawah naungan Yayasan Prawitama pada tahun 1996 di bekas gudang KUD. Kompetensi keahlian yang pertama dibuka pada saat itu adalah sekretaris dengan jumlah hanya 34 siswa.</p>

                           <p class="paragraf">Seiring berjalannya waktu, jumlah siswa SMK Wikrama Bogor setiap tahunnya terus bertambah. Sehingga pada tahun 2001, secara bertahap SMK Wikrama Bogor menempati gedung yang lebih luas diatas tanah ± 5000m2, berlokasi di Jalan Raya Wangun Kelurahan Sindangsari Kota Bogor. Hingga saat ini, SMK Wikrama Bogor  memiliki 1596 siswa dengan 51 guru pendidik.</p>
                           
                           
                           <p class="paragraf">Kompetensi keahlian di SMK Wikrama Bogor pun terus berkembang. SMK Wikrama Bogor membuka 7 kompetensi keahlian, diantaranya (1) Otomatisasi dan Tata Kelola Perkantoran; (2) Teknik Komputer dan Jaringan; (3) Rekayasa Perangkat Lunak; (4) Multimedia; (5) Bisnis Daring dan Pemasaran; (6) Tata Boga; dan (7) Perhotelan.</p>
                           <p class="paragraf">Kesuksesan SMK Wikrama Bogor saat ini tentunya tidak lepas dari sejarah SMK Wikrama Bogor  mulai dari membentuk visi dan misi, kerja keras hingga diakui dunia internasional hingga prestasi dan pengharagaan yang didapatkan SMK Wikrama Bogor sejak awal didirikan. Berikut sejarah yang dilalui SMK Wikrama Bogor berdasarkan urutan tahun.</p>  
                           <h4>Visi</h4>
                           <p class="paragraf">Menjadi sekolah kejuruan teladan nasional yang berbudaya lingkungan, berkarakter kebangsaan, berbasis teknologi informasi dan mampu memenuhi kebutuhan dunia kerja.</p>
                           
                           <h4>Misi</h4>
                           <p class="paragraf">1. Mendidik anak bangsa dengan hati dan teknologi sehingga memenuhi kebutuhan mutu dunia kerja.</p>
                           <p class="paragraf">2. Mewujudkan sekolah sebagai benteng moralitas bangsa.</p>
                           <p class="paragraf">3. Membangun kebersamaan sosial, jiwa kewirusahaan, dan gerakan cinta tanah air dan lingkungan.</p>
                           <h4>Kebijakan Mutu</h4>
                           <p class="paragraf">Dengan berkomitmen terhadap kepuasan pelanggan, peraturan perundangan, sistem mananjemen mutu, dan perbaikan berkelanjutan, SMK Wikrama menghasilkan lulusan yang kompeten dan luaran sekolah yang berdaya  guna.</p>
                           <h4>Moto</h4>
                           <p class="paragraf">“Ilmu yang amaliah, amal yang ilmiah, akhlaqul karimah”</p>
